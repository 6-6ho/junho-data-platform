# Themes to exclude from RS calculation
EXCLUDE_THEMES = {"스테이블", "금코인"}


def seed():
    conn = psycopg2.connect(DATABASE_URL)
    conn.autocommit = True
    cur = conn.cursor()

    try:
        # Create tables if not exist
        cur.execute(open("/app/../infra/postgres-init/07_theme_mapping.sql" if os.path.exists("/app") else "infra/postgres-init/07_theme_mapping.sql").read())
    except Exception:
        pass

    # Clear existing data
    cur.execute("DELETE FROM coin_theme_mapping")
    cur.execute("DELETE FROM theme_master")

    # Collect unique themes
    themes = sorted(set(row[0] for row in THEME_DATA))
    theme_ids = {}

    for theme_name in themes:
        exclude = theme_name in EXCLUDE_THEMES
        cur.execute(
            "INSERT INTO theme_master (theme_name, exclude_from_rs) VALUES (%s, %s) RETURNING theme_id",
            (theme_name, exclude)
        )
        theme_ids[theme_name] = cur.fetchone()[0]

    # Insert coin-theme mappings
    inserted = 0
    skipped = 0
    for theme_name, name_kr, symbol, listed_period, note in THEME_DATA:
        tid = theme_ids[theme_name]
        try:
            cur.execute(
                "INSERT INTO coin_theme_mapping (symbol, theme_id, name_kr, listed_period, note) VALUES (%s, %s, %s, %s, %s)",
                (symbol, tid, name_kr, listed_period, note)
            )
            inserted += 1
        except Exception:
            # Duplicate (same symbol + theme) — skip
            skipped += 1

    print(f"✅ Seeded {len(themes)} themes, {inserted} mappings ({skipped} skipped duplicates)")

    # Summary
    cur.execute("""
        SELECT t.theme_name, COUNT(c.symbol) as cnt
        FROM theme_master t
        LEFT JOIN coin_theme_mapping c USING(theme_id)
        GROUP BY t.theme_name
        ORDER BY cnt DESC
    """)
    for row in cur.fetchall():
        print(f"  {row[0]}: {row[1]} coins")

    cur.close()
    conn.close()


